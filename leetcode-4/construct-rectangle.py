def constructRectangle(area):
        root = area ** 0.5
        options = []
        for i in range(1, int(root)+1):
            if area//i * i == area: 
                options.append([area//i, i])
        return min(options, key=lambda x: x[0] - x[1])


print(constructRectangle(4))

